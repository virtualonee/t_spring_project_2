DROP TABLE IF EXISTS Book;
DROP TABLE IF EXISTS Person;


CREATE TABLE Person(
    id int PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    fio varchar(100) NOT NULL,
    date varchar(100)
);

CREATE TABLE Book(
    id int PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    person int REFERENCES Person ON DELETE SET NULL,
    name varchar(150) NOT NULL,
    author varchar(150),
    year varchar(10)
);

